module R2_Menu_Sound
  SONG = "Theme1"
end

# ╔════════════════════════════════════════════════════════════════════╗
# ║                      End of editable region                        ║
# ╚════════════════════════════════════════════════════════════════════╝

class Scene_Menu < Scene_MenuBase
  #--------------------------------------------------------------------------
  # * Start Processing
  #--------------------------------------------------------------------------
  def start
    super
    song = RPG::BGM.new(R2_Menu_Sound::SONG, 100, 100)
    song.play
    create_command_window
    create_gold_window
    create_status_window
  end
end

class Scene_Map < Scene_Base
  def call_menu
    Sound.play_ok
    @last_bgm = RPG::BGM.last
    SceneManager.call(Scene_Menu)
    RPG::BGM.stop
    Window_MenuCommand::init_command_position
  end
  alias r2_update_map_scene update_scene
  def update_scene
    r2_update_map_scene
    return if SceneManager.scene_is?(Scene_Menu)
    @last_bgm.play(@last_bgm.pos) if @last_bgm != nil
    @last_bgm = nil
  end
end

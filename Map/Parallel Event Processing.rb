class Game_Event < Game_Character
  def update
    super
    check_event_trigger_auto
    return unless @interpreter
    @interpreter.update
    @interpreter.setup(@list, @event.id) unless @interpreter.running?
    @interpreter.update if @interpreter.running?
  end
end

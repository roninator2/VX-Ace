module R2_MESSAGE_CLOSE
	AUDSND = "Close1"	# must be in quotes
end
class Window_Message < Window_Base
  def close_and_wait
    close
    Fiber.yield until all_close?
    RPG::SE.new(R2_MESSAGE_CLOSE::AUDSND, 80, 0).play
  end
end
